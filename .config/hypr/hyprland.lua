-- See https://wiki.hypr.land/Configuring/Basics/Autostart/

-- Autostart necessary processes (like notifications daemons, status bars, etc.)
-- Or execute your favorite apps at launch like this:
--
hl.on("hyprland.start", function()
	-- autostart important daemons
	hl.exec_cmd("dbus-update-activation-environment --systemd WAYLAND_DISPLAY XDG_CURRENT_DESKTOP")
	hl.exec_cmd("systemctl --user import-environment WAYLAND_DISPLAY XDG_CURRENT_DESKTOP")
	hl.exec_cmd("hyprctl setcursor Bibata-Modern-Ice 24") -- set sursor style
	hl.exec_cmd("$HOME/scripts/xdg-portal-hyprland") -- desktop portal with window sharing
	hl.exec_cmd("$HOME/.azotebg-hyprland") -- set wallpaper

	-- # Source device-specific configs
	-- source = ~/.config/hypr/device/device.conf

	hl.exec_cmd("gsettings set org.gnome.desktop.interface color-scheme prefer-dark") -- set GTK3/4 dark theme
	hl.exec_cmd("systemctl --user start hyprpolkitagent")
	hl.exec_cmd("gnome-keyring-daemon --start --components=secrets") -- secrets daemon (will switch to keepassxc at some point)
	hl.exec_cmd(Shell)
	-- hl.exec_cmd("systemctl --user restart mako.service") # notifications
	-- hl.exec_cmd("wlsunset -S 8:00 -s 20:00")
	hl.exec_cmd("/usr/lib/kdeconnectd") -- kdeconnect daemon - start AFTER nofiication daemon
	hl.exec_cmd("flameshot") -- screenshots
	hl.exec_cmd("wl-paste --type text --watch cliphist store") -- Stores only text data
	hl.exec_cmd("wl-paste --type image --watch cliphist store") -- Stores only image data
	-- hl.exec_cmd('wl-paste -t text -w sh -c 'v=$(cat); cmp -s <(xclip -selection clipboard -o)  <<< "$v" || xclip -selection clipboard <<< "$v"'') -- sync wayland and x11 clipboards
	-- hl.exec_cmd("hypridle") -- idle management daemon
end)

